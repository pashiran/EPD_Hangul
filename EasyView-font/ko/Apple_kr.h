// Helper functions
// Get glyph data by index (0~359)
inline const uint8_t* Apple_kr_getGlyph(uint16_t index) {
  if (index >= 360) return nullptr;
  return &Apple_kr_Bitmaps[index * 32];
}

// Get cho (초성) glyph by bul and index
inline const uint8_t* Apple_kr_getCho(uint8_t bul, uint8_t index) {
  if (bul >= 8 || index >= 20) return nullptr;
  uint16_t glyphIndex = 0 + bul * 20 + index;
  return Apple_kr_getGlyph(glyphIndex);
}

// Get jung (중성) glyph by bul and index
inline const uint8_t* Apple_kr_getJung(uint8_t bul, uint8_t index) {
  if (bul >= 4 || index >= 22) return nullptr;
  uint16_t glyphIndex = 160 + bul * 22 + index;
  return Apple_kr_getGlyph(glyphIndex);
}

// Get jong (종성) glyph by bul and index
inline const uint8_t* Apple_kr_getJong(uint8_t bul, uint8_t index) {
  if (bul >= 4 || index >= 28) return nullptr;
  uint16_t glyphIndex = 248 + bul * 28 + index;
  return Apple_kr_getGlyph(glyphIndex);
}

